        <h3>Tip Guidelines</h3>
        <p>
          Please remember the following when writing a tip:
        </p>
        <ol>
          <li>
            Tips should be limited in scope as much as possible &mdash; generally a single piece of advice, recommendation, or opinion. This isn't the place to write a lengthy article or comprehensive guide (though feel free to link to such guides).
          </li>
          <li>
            Make titles as descriptive as possible. For example, "Make dining reservations 180 days out" is a better title than "Dining reservation tip".
          </li>
          <li>
            Please be as specific as possible with the topic of your tip. For example, <code>Parks > Magic Kingdom > Dining</code> is better than just <code>Parks > Magic Kingdom</code> if your tip is about an MK restaurant. 
          </li>
          <li>
            Add tags to further describe what your tip is about. Use existing tags (from autocomplete) when possible, but feel free to make new tags as-needed.
          </li>
        </ol>
      </section>